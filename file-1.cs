Console.Writeline(); //appends new line after the parameter
Console.Write(); //doesnt append new line
Console.WriteLine('a'); //char literal
Console.WriteLine("Hello"); //string, USE DOUBLE QUOTES
Console.WriteLine(2.455f); //float, can use F too ~6-9 digits
Console.WriteLine(24.555); //double, ~15-17 digits
Console.WriteLine(32.444m); //decimal, ~28-29 digits
Console.WriteLine(true); //or false

//note
// prefer string datatype when working with numerical data that does not involve arithmetic, say postal codes for example
// use bool for concept of true and false, string if you need actual "true" or "false"
